<script>
  export default {
    name: "Home",
    title: "Healthcare App - Home page",
    data () {
      return {
        index: 0
      };
    },
    methods: {
      carouselFunction (e) {
        const carousel = e.parentNode.parentNode.parentNode,
              track = e.parentNode.previousSibling;
        let width = carousel.offsetWidth;
        
        // if previous button is clicked
        if (e.classList.contains('next')) {
          const prev = e.previousSibling;
          this.index = this.index + 1;
          prev.classList.add("show");
          track.style.transform = "translateX(" + this.index * -width + "px)";
          if (track.offsetWidth - (this.index * width) < this.index * width) {
            e.classList.add("hide");
          }
        }
        // If next button is clicked
        if (e.classList.contains('prev')) {
          const next = e.nextSibling;
          this.index = this.index - 1;
          next.classList.remove("hide");
          if (this.index === 0) {
            e.classList.remove("show");
          }
          track.style.transform = "translateX(" + this.index * -width + "px)";
        }
      }
    }
  };
</script>

